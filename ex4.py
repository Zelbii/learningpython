#fazer um programa que leia algo que foi escrito e motre o seu tipo primitivo e algumas outras informações.
a=input('Digite algo')
print('o tipo primitivo desse valor é ',type(a))
print('só tem espaço?',a.isspace())
print('é numero?',a.isnumeric())
print('é alfabetico?',a.isalpha())
print('é alfanumérico?',a.isalpha())